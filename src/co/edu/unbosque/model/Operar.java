package co.edu.unbosque.model;

public class Operar {
    public int Suma(int dato1, int dato2) {
        return dato1 + dato2;
    }

    public int resta(int num1, int num2) {
        return num1 - num2 ;
    }

    public int dividir(int num1, int num2) {

        return num1 / num2 ;
    }

    public int mult(int num1, int num2) {

        return num1 * num2 ;
    }
}
